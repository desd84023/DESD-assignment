 #include <stdio.h>
 
int main()
{
    int value=20;
     
    printf("Decimal value is: %d\n",value);
    printf("Octal value is: %o\n",value);
    printf("Hexadecimal value is in small letters: %x\n",value);
    printf("Hexadecimal value is  in capital letters): %X\n",value);
     
    return 0;   
}

